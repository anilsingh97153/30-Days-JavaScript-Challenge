let a = 10;
let b = 30;

console.log(`Sum of ${a} and ${b} = ${a+b}`);

console.log(`Subtraction = ${b-a}`);

console.log(`Mutiplication of ${a} and ${b} = ${a*b}`);

console.log(`${b} divided by ${a} = ${b / a}`);

console.log(`${b} divided by ${a}, remainder = ${b % a}`);


// OUTPUT
