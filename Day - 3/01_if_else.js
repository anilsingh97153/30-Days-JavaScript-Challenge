// Task - 1: Check if number is +ve, -ve, or 0.

const num = -15;

if (num < 0) {
  console.log(`${num} is a negative number.`);
} else if (num > 0) {
  console.log(`${num} is a positive number.`);
} else {
  console.log(`${num} is zero.`);
}


