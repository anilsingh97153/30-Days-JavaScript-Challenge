// Task - 4: Create variables of different data types (number, string, boolean, object, array) and log each varaible's type using the 'typeof' operator.
let num = 10;
let str = "Hello world!";
let bool = true;
let obj = {
  myName: "Anil Singh",
  myAge: 23,
};
let arr = [1, 2, 3, 4, 5];

console.log(`num: ${num} and typeof num: ${typeof(num)}`);
console.log(`str: ${str} and typeof str: ${typeof(str)}`);
console.log(`bool: ${bool} and typeof bool: ${typeof(bool)}`);
console.log(`obj: ${obj} and typeof obj: ${typeof(obj)}`);
console.log(`arr: ${arr} and typeof arr: ${typeof(arr)}`);




